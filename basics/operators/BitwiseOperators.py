# Python
x = 9
print(x, "= ", format(x, "08b"))
x = x << 1
print(x, "= ", format(x, "08b"))
x = x >> 2
print(x, "= ", format(x, "08b"))
x = x ^ 4
print(x, "= ", format(x, "08b"))
x = x | 9
print(x, "= ", format(x, "08b"))
x = x & 1
print(x, "= ", format(x, "08b"))
x = ~x
print(x, "= ", format(x, "08b"))
